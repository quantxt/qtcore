package com.quantxt.model.search;

public class FileDocumentDto {
    private String uuid;
    private String name;

    public FileDocumentDto(){

    }
    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
